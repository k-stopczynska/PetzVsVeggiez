package com.ainspiring.utils.prototypes;

public class Prototype {
    public Prototype() {}

        public String name;
        public String image;
        public int health;
        public int cost;

    public String getName() {
        return name;
    }

    public void setName(String image) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public int getHealth() {
        return health;
    }

    public void setHealth(int health) {
        this.health = health;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }


}
